package Calculadora

fun soma(N1: Double , N2: Double) {
    return print("A soma entre os numeros digitados é: ${N1 + N2} ")
}

fun sub(N1: Double , N2: Double) {
    return print("A subtração entre os numeros digitados é: ${N1 - N2} ")
}

fun mult(N1: Double , N2: Double) {
    return print("A multiplicação entre os numeros digitados é: ${N1 * N2} ")
}

fun divisa(N1: Double , N2: Double) {

    return print("A divisão entre os numeros digitados é: ${N1 / N2} ")
}
